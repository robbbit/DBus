package com.creditease.dbus.stream.common.appender.spout.cmds;

/**
 * Created by Shrimp on 16/6/21.
 */
public class TopicResumeCmd extends CtrlCommand {

    private String datasource;
    private String schema;
    private String table;
    private String status;
    private int version;
    private String topic;
    private int batchId;
    private String commandSender;

    @Override
    public Object execCmd(CommandHandler handler, Object... args) {
        return handler.handleCmd(this, args);
    }

    public int getBatchId() {
        return batchId;
    }

    public void setBatchId(int batchId) {
        this.batchId = batchId;
    }

    public String getDatasource() {
        return datasource;
    }

    public void setDatasource(String datasource) {
        this.datasource = datasource;
    }

    public String getSchema() {
        return schema;
    }

    public void setSchema(String schema) {
        this.schema = schema;
    }

    public String getTable() {
        return table;
    }

    public void setTable(String table) {
        this.table = table;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getVersion() {
        return version;
    }

    public void setVersion(int version) {
        this.version = version;
    }

    public String getTopic() {
        return topic;
    }

    public void setTopic(String topic) {
        this.topic = topic;
    }

    public String getCommandSender() {
        return commandSender;
    }

    public void setCommandSender(String commandSender) {
        this.commandSender = commandSender;
    }
}
