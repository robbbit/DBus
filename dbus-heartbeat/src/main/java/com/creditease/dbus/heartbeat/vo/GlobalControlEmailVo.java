package com.creditease.dbus.heartbeat.vo;

import java.io.Serializable;
import java.util.Map;

public class GlobalControlEmailVo implements Serializable {
    private static final long serialVersionUID = 2856446223290442863L;
    private String from;
    private long id;
    private String timestamp;
    private String type;
    private Map<String, Map<String, Object>> payload;

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Map<String, Map<String, Object>> getPayload() {
        return payload;
    }

    public void setPayload(Map<String, Map<String, Object>> payload) {
        this.payload = payload;
    }
}
